let nivelEscolhido;
let indiceAtual = 0;
let pontos = 0;
let perguntasAtuais = [];

function IniciarQuiz() {
  const nivel = document.querySelector('input[name="nivel"]:checked');

  if (!nivel) {
    alert("Escolha um nível!");
    return;
  }

  nivelEscolhido = nivel.value;

  perguntasAtuais = perguntas[nivelEscolhido];

  perguntasAtuais.sort(() => Math.random() - 0.5); /* embaralhar perguntas*/

  indiceAtual = 0;
  pontos = 0;

  mostrarPergunta();
}


function mostrarPergunta() {
  const container = document.getElementById("container");

  const perguntaAtual = perguntasAtuais[indiceAtual];

  container.innerHTML = `
        <h1 id= "pergunta">${perguntaAtual.pergunta}</h1>
          <img id= "img" src= "${perguntaAtual.imagem}">
        <div class="opcoes">
            ${perguntaAtual.opcoes
              .map(
                (opcao, indice) => `
                <button
                    class="opcao"
                    onclick="responder(${indice})">
                    ${opcao}
                </button>
            `
              )
              .join("")}
        </div>

        <p class= "contador">
            Pergunta ${indiceAtual + 1}
            de ${perguntasAtuais.length}
        </p>
    `;
}

function responder(indiceEscolhido) {
  const correta = perguntasAtuais[indiceAtual].correta;

  if (indiceEscolhido === correta) {
    pontos++;
  }

  indiceAtual++;

  if (indiceAtual < perguntasAtuais.length) {
    mostrarPergunta();
  } else {
    mostrarResultado();
  }
}

function mostrarResultado() {
  const container = document.getElementById("container");

  const porcentagem = Math.round((pontos / perguntasAtuais.length) * 100);

  let mensagem = "";

  if (porcentagem === 100) {
    imagem = "https://i.pinimg.com/originals/13/d4/de/13d4de64e73e78ec458aed8231768293.gif"; 
    mensagem = "Você é um verdadeiro Na'vi!";
  } else if (porcentagem >= 70) {
    imagem = "https://i.pinimg.com/originals/b2/75/81/b27581acb400adb873c47e6971a61cc6.gif";
    mensagem = "Ótimo conhecimento sobre Avatar!";
  } else if (porcentagem >= 50) {
    imagem = "https://i.pinimg.com/originals/dc/09/75/dc09758f8e8bc92138455d0f2492133f.gif";
    mensagem = "Bom trabalho!";
  } else {
    imagem ="https://i.pinimg.com/originals/22/d7/db/22d7db8597df6f74d1d0047658507133.gif";
    mensagem = "Hora de rever os filmes!";
  }

  container.innerHTML = `
        <h1 id="qf">Quiz Finalizado</h1>

        <h2 id="pontuacao">Você acertou ${pontos}/${perguntasAtuais.length}</h2>

        <h2 id="porc">${porcentagem}%</h2>
        <img id="if" src=${imagem}>
        <p id="msg">${mensagem}</p>

        <button class="butao" onclick="location.reload()">
            Jogar Novamente
        </button> 
    `;
}